#include <stdio.h>

/**
 * main - Entry Point
 *
 * Description: 'to print positive, negative values at random'
 *
 * Return: return n
 */

int main(void)
{
	int n;

	if (n > 0)
	{
		printf("The number is positive \n");
	}

	else if (n < 0)
	{
		printf("The number is negative \n");
	}

	else
	{
		printf("The number is zero \n");
	}

	return (0);
}
